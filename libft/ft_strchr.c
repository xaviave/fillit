#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int i;

	i = 0;
	while (c != str[i] && str[i])
		i++;
	if (c == str[i])
		return (char *)(&str[i]);
	return (char *)(NULL);
}
